// Bitwise Operators Full Example

let a = 5;  // Binary: 101
let b = 3;  // Binary: 011

// 1. AND (&)
// இரண்டும் 1 இருந்தா மட்டும் 1
console.log("AND (&):", a & b);  

// 2. OR (|)
// ஒன்றாவது 1 இருந்தா போதும்
console.log("OR (|):", a | b);   

// 3. XOR (^)
// வேறுபட்டால் மட்டும் 1
console.log("XOR (^):", a ^ b);  

// 4. NOT (~)
//bit reverse ஆகும்
console.log("NOT (~):", ~a);     

// 5. LEFT SHIFT (<<)
// Multiply by 2
console.log("LEFT SHIFT (<<):", a << 3);  

// 6. RIGHT SHIFT (>>)
// Divide by 2
console.log("RIGHT SHIFT (>>):", a >> 1); 
